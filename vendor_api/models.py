class Vendor(models.Model):
    name = models.CharField(max_length=255)
    contact_details = models.TextField()
    address = models.TextField()
    vendor_code = models.CharField(max_length=50, unique=True)
    on_time_delivery_rate = models.FloatField(default=0)
    quality_rating_avg = models.FloatField(default=0)
    average_response_time = models.FloatField(default=0)
    fulfillment_rate = models.FloatField(default=0)

    # Add this method to customize the representation of the Vendor object
    def __str__(self):
        return self.name


    skip_update_metrics = False

    def save(self, *args, **kwargs):
        if not self.skip_update_metrics:
            self.skip_update_metrics = True
            super().save(*args, **kwargs)
            from .utils import update_vendor_metrics
            update_vendor_metrics(self)
            self.skip_update_metrics = False
        else:
            super().save(*args, **kwargs)

    
class PurchaseOrder(models.Model):
    po_number = models.CharField(max_length=50, unique=True)
    vendor = models.ForeignKey('Vendor', on_delete=models.CASCADE)
    order_date = models.DateTimeField()
    delivery_date = models.DateTimeField()
    items = models.JSONField()
    quantity = models.IntegerField()
    status = models.CharField(max_length=50)
    quality_rating = models.FloatField(null=True, blank=True)
    issue_date = models.DateTimeField()
    acknowledgment_date = models.DateTimeField(null=True, blank=True)

    def save(self, *args, **kwargs):
        super().save(*args, **kwargs)
        from .utils import update_vendor_metrics  # Import inside the function to avoid circular import
        update_vendor_metrics(self.vendor)

